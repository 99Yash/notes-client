import Head from 'next/head';
import { Inter } from '@next/font/google';
import Navbar from '@/components/Navbar';
import Link from 'next/link';
import { useEffect, useState } from 'react';
import { useAppDispatch, useAppSelector } from '@/hooks/redux';
import {
  NoteState,
  reset,
  setNotes,
  deleteNote,
} from '@/store/slices/notes.slice';
import { Note } from '@/interfaces/note.interface';
import axios from 'axios';
import { setUser } from '@/store/slices/user.slice';
import SingleNote from '@/components/SingleNote';
import { useRouter } from 'next/router';

const inter = Inter({ subsets: ['latin'] });

export default function Home() {
  const notes = useAppSelector((state) => state.notes);
  const user = useAppSelector((state) => state.user);
  const dispatch = useAppDispatch();
  const router = useRouter();

  useEffect(() => {
    const token = localStorage.getItem('token');
    if (!token) {
      dispatch(reset());
    }
  }, [user, dispatch]);

  useEffect(() => {
    const token = localStorage.getItem('token');
    if (!token) return;

    const fetchNotes = async () => {
      try {
        const { data } = await axios.get<Note[]>(
          'http://localhost:5000/api/notes'
        );
        dispatch(setNotes({ notes: data }));
      } catch (error) {
        console.error(error);
      }
    };
    fetchNotes();
    router.push(`/${user.user?._id}/notes`);
  }, [dispatch, user, router]);

  return (
    <>
      <Head>
        <title>Notes App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`${inter.className}`}>
        {notes.notes?.length > 0 &&
          notes.notes?.map((note: Note) => {
            return <SingleNote key={note._id} note={note} />;
          })}
        {user.isLoggedIn && (
          <Link
            href="/add-note"
            className="fixed bottom-0 right-0 p-4 mx-12 my-8 bg-yellow-100 text-black rounded-full duration-150 shadow-xl"
          >
            +
          </Link>
        )}
      </main>
    </>
  );
}
